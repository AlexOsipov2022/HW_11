package HW_11;

public class Methods {
    public static int additionMethod(Integer a, Integer b) {
        return a + b;
    }
    public static int subtractionMethod(Integer a, Integer b) {
        return a - b;
    }
    public static int multiplicationMethod(Integer a, Integer b) {
        return a * b;
    }
    public static int divisionMethod(Integer a, Integer b) {
        return a / b;
    }
}
